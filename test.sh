#!/usr/bin/sh

make -j16

file=tempfile
expected_file=expectedfile

cat << EOF > $file
GLOBAL _start

EXTERN exit


_start: RAM_AL 0x00 PUSHA CALLR \$main POPA CALL exit       




main: RAM_AL 0x00 PUSHA RAM_AL 0x01 PUSHA RAM_BL 0x00 RAM_AL 0x01 SUB POPB SUB JMPRZ $_000 RAM_AL 0x00 PUSHA RAM_BL 0x01 RAM_AL 0x02 SUB JMPRZ $_001 RAM_BL 0x01 RAM_AL 0x03 SUB JMPRZ $_001 INCSP RAM_AL 0x01 PUSHA _001: POPA CMPA JMPRZ $_000 INCSP RAM_AL 0x01 PUSHA _000: RAM_AL 0x00 PUSHA RAM_AL 0x01 CMPA JMPRZ $_002 RAM_AL 0x00 PUSHA RAM_AL 0x02 CMPA JMPRZ $_003 RAM_AL 0x03 CMPA JMPRZ $_003 INCSP RAM_AL 0x01 PUSHA _003: POPA CMPA JMPRZ $_002 INCSP RAM_AL 0x01 PUSHA _002: INCSP RAM_AL 0x01 PUSHA RAM_AL 0x01 CMPA JMPRNZ $_004 RAM_AL 0x02 CMPA JMPRNZ $_004 INCSP RAM_AL 0x00 PUSHA _004: INCSP INCSP RET

   
EOF

cat << EOF > $expected_file
GLOBAL _start

EXTERN exit


_start: RAM_AL 0x00 PUSHA CALLR \$main POPA CALL exit

main: RAM_AL 0x00 PUSHA RAM_AL 0x01 PUSHA RAM_BL 0x00 RAM_AL 0x01 SUB POPB SUB JMPRZ RAM_AL 0x00 PUSHA RAM_BL 0x01 RAM_AL 0x02 SUB JMPRZ RAM_BL 0x01 RAM_AL 0x03 SUB JMPRZ INCSP RAM_AL 0x01 PUSHA
_001: POPA CMPA JMPRZ INCSP RAM_AL 0x01 PUSHA
_000: RAM_AL 0x00 PUSHA RAM_AL 0x01 CMPA JMPRZ RAM_AL 0x00 PUSHA RAM_AL 0x02 CMPA JMPRZ RAM_AL 0x03 CMPA JMPRZ INCSP RAM_AL 0x01 PUSHA
_003: POPA CMPA JMPRZ INCSP RAM_AL 0x01 PUSHA
_002: INCSP RAM_AL 0x01 PUSHA RAM_AL 0x01 CMPA JMPRNZ RAM_AL 0x02 CMPA JMPRNZ INCSP RAM_AL 0x00 PUSHA
_004: INCSP INCSP RET
EOF

found=$(./asm_formatter $file)

echo TEST asm_formatter
if [ "$expected" != "$found" ]; then
  git -P diff --no-index "$expected_file" <(echo "$found")
fi
echo TEST END

rm $file $expected_file
